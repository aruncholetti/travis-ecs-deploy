#! /bin/bash

export PATH=$PATH:$HOME/.local/bin

CLUSTER_NAME=ECS_CLUSTER_NAME_${TRAVIS_BRANCH}
SERVICE_NAME=ECS_SERVICE_NAME_${TRAVIS_BRANCH}

echo "Deploying $TRAVIS_BRANCH on service $SERVICE_NAME on cluster $CLUSTER_NAME with image $REMOTE_IMAGE_URL:$TRAVIS_BRANCH"
../lib/ecs-deploy -t 300 -n $SERVICE_NAME -c $CLUSTER_NAME -i $REMOTE_IMAGE_URL:$TRAVIS_BRANCH
